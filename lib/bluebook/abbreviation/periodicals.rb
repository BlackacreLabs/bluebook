module Bluebook
  module Abbreviation
    def self.periodical(journal)
      # Periodical listed by name
      if abbrev = PERIODICALS[journal]
        return abbrev

      # Compose an abbreviation
      else
        # Remove omitted words
        omitted = OMITTED.reduce(journal) do |mem, om|
          mem.gsub(/\b#{om}\b/i, '').gsub(/\s+/, ' ').strip
        end

        # Just one word left? Stop abbreviating.
        return omitted if omitted.split(/\b/).count == 1

        # More than one word. Apply abbreviations.
        abbrevd = PERIODICAL_ABBREVIATIONS.reduce(omitted) do |mem, pair|
          from, to = pair
          from = /\b#{from}\b/ unless from.is_a?(Regexp)
          mem = mem.gsub(from, to).gsub(/\s+/, ' ').strip
        end
        
        space_abbreviations(abbrevd)
      end
    end

    NON_INSTITUTIONAL = ['L.J.', 'L. Rev.']

    CONTIGUOUS_ABBREVS = /\b(\w)\. (\w)\./

    def self.space_abbreviations(input)
      # Collapse contiguous single-letter abbreviations
      while CONTIGUOUS_ABBREVS.match(input)
        input.gsub!(CONTIGUOUS_ABBREVS, '\1.\2.')
      end

      # Single-letter abbreviations of institution names are not to be
      # collapsed with single-letter abbreviations of the publication
      # name.
      #
      # This is a kludge to catch the most common cases:
      #
      # Something-or-Other Law Review
      # Something-or-Other Law Journal
      #
      NON_INSTITUTIONAL.reduce(input) do |mem, non|
        if mem.end_with?(non)
          mem.sub(".#{non}", ". #{non}").strip
        else
          mem
        end
      end
    end

    OMITTED = %w{a at in of the}

